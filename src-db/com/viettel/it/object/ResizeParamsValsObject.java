package com.viettel.it.object;

public class ResizeParamsValsObject {

	private String createReadWriteCmd;
	private String resizeCmd;
	
	public ResizeParamsValsObject() {
		createReadWriteCmd = "";
		resizeCmd = "";
	}
	
	public String getCreateReadWriteCmd() {
		return createReadWriteCmd;
	}
	
	public void setCreateReadWriteCmd(String createReadWriteCmd) {
		this.createReadWriteCmd = createReadWriteCmd;
	}
	
	public String getResizeCmd() {
		return resizeCmd;
	}
	
	public void setResizeCmd(String resizeCmd) {
		this.resizeCmd = resizeCmd;
	}
	
	
}
