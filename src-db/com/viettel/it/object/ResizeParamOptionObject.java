package com.viettel.it.object;

public class ResizeParamOptionObject {

	private String rotateMonth;
	private String tbsName;
	private String rotateMonthCondition;
	private String tableNameCondition;
	
	public String getTbsName() {
		return tbsName;
	}

	public void setTbsName(String tbsName) {
		this.tbsName = tbsName;
	}

	public String getRotateMonthCondition() {
		return rotateMonthCondition;
	}

	public void setRotateMonthCondition(String rotateMonthCondition) {
		this.rotateMonthCondition = rotateMonthCondition;
	}

	public String getRotateMonth() {
		return rotateMonth;
	}

	public void setRotateMonth(String rotateMonth) {
		this.rotateMonth = rotateMonth;
	}

	public String getTableNameCondition() {
		return tableNameCondition;
	}

	public void setTableNameCondition(String tableNameCondition) {
		this.tableNameCondition = tableNameCondition;
	}

}
